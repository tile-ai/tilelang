bool ParallelOpNode::ValidateCandidateAgainstFragments(
    const Fragment &candidate, const LayoutInferArgs &layout_args,
    bool throw_on_error, bool check_forward_index,
    const Buffer &source_buffer) const {
  auto vars =
      loop_vars_.Map([](const IterVar &iv) { return PrimExpr(iv->var); });
  for (const auto &buffer : access_order_) {
    const auto &access = GetAccessInfo(buffer);
    if (!layout_args.layout_map.count(buffer))
      continue;
    if (auto info = reducer_info_map_.Get(buffer->data);
        info && info.value()->rep == ReducerRepType::ALL)
      continue;
    auto fragment = layout_args.layout_map[buffer].as<Fragment>().value();
    std::ostringstream oss;
    bool success = true;
    if (access.is_read &&
        !ProveFragmentContains(candidate, fragment, vars, access.indices,
                               analyzer_, check_forward_index)) {
      if (throw_on_error) {
        oss << "Layout infer conflict between " << buffer << " and "
            << source_buffer << " in T.Parallel loop:" << '\n'
            << "    loop " << candidate->DebugOutput() << '\n'
            << "    fragment " << fragment->DebugOutput() << '\n';
      }
      success = false;
    }
    // Fragment writes need exact owner-thread compatibility. Coverage alone
    // allows extra loop threads to write local slots that belong to different
    // logical fragment elements under the buffer layout.
    if (access.is_write &&
        (!ProveFragmentContains(fragment, candidate, access.indices, vars,
                                analyzer_, check_forward_index) ||
         !ProveFragmentContains(candidate, fragment, vars, access.indices,
                                analyzer_, check_forward_index))) {
      if (throw_on_error) {
        oss << "Layout infer conflict between " << buffer << " and "
            << source_buffer << " in T.Parallel loop:" << '\n'
            << "    loop " << candidate->DebugOutput() << '\n'
            << "    fragment " << fragment->DebugOutput() << '\n';
      }
      success = false;
    }
    if (!success) {
      if (throw_on_error) {
        throw LayoutConflictException(oss.str());
      }
      return false;
    }
  }
  return true;
}

Fragment ParallelOpNode::ComputeLoopLayoutFromBuffer(
    const Buffer &buffer, const LayoutInferArgs &layout_args) const {
  Fragment src_layout = layout_args.layout_map[buffer].as<Fragment>().value();
  DLOG(INFO) << "[compute_loop_layout_from_buffer] infer from buffer `"
             << buffer << "` of layout " << src_layout->DebugOutput() << '\n';

  Fragment result;

  if (IsCommonAccessIndice(buffer)) {
    result = src_layout;
  } else {
    Var rep("_rep");
    auto rep_iter =
        IterVar({0, src_layout->ReplicateExtent()}, rep, IterVarType::kDataPar);
    PrimExpr loop_var_to_thread =
        src_layout->ForwardThread(GetAccessInfo(buffer).indices, rep);
    loop_var_to_thread = analyzer_.Simplify(loop_var_to_thread);
    PostOrderVisit(loop_var_to_thread, [&](const ObjectRef &objref) {
      if (auto opt_var = objref.as<Var>();
          opt_var && inner_vars_.count(*opt_var)) {
        std::ostringstream oss;
        oss << "Cannot lower access to thread-distributed fragment `"
            << buffer->name
            << "`: its elements are spread across CUDA threads, so the "
               "non-parallel loop variable `"
            << *opt_var
            << "` cannot be used to address them safely (thread map: "
            << loop_var_to_thread
            << "). Use T.reduce_sum for reductions, T.Parallel for elementwise "
               "access, or stage through shared memory to change the layout.";
        throw LayoutConflictException(oss.str());
      }
    });

    try {
      result = Fragment(loop_vars_, {}, loop_var_to_thread, rep_iter)
                   ->BindThreadRange(layout_args.thread_bounds);
    } catch (const Error &err) {
      std::ostringstream msg;
      msg << "Layout inference for buffer `" << buffer->name
          << "` failed inside `T.parallel` loop.";

      msg << "\nUnderlying TVM error: " << err.what();
      msg << "\nProblematic loop AST:\n " << root_;
      msg << "\nHint: ensure the loop extent divides the thread binding or "
             "adjust the fragment mapping.";
      LOG(FATAL) << msg.str();
    }
  }
  DLOG(INFO) << "[compute_loop_layout_from_buffer] ... and get "
             << result->DebugOutput() << '\n';
  // Lei: This is a tradeoff, disable it for now.
  // // Try DeReplicate first to reduce replication if possible.
  // Fragment dereplicated_layout = candidate_from_buffer->DeReplicate();
  // if (ValidateCandidateAgainstFragments(
  //         dereplicated_layout, T, /*throw_on_error=*/false,
  //         /*check_forward_index=*/false,
  //         /*source_buffer=*/read_source_buffer)) {
  //   candidate_from_buffer = dereplicated_layout;
  // }
  return result;
}

Fragment
ParallelOpNode::ComputePlanCandidate(const LayoutInferArgs &layout_args) const {
  // Vectorize Size must be aware of the buffer_remap
  // As the pass will do post processing to the layout
  auto maybe_remapped_root_ = IfBufferRemapLoopGenerator::run(
      root_, layout_args.buffer_remap, layout_args.layout_map);
  int vector_size = GetVectorizeSize(maybe_remapped_root_, layout_args.analyzer,
                                     layout_args.layout_map, reducer_info_map_);
  DLOG(INFO) << "[PlanLoopPartition] vector_size = " << vector_size << '\n';

  PrimExpr loop_total_size = 1;
  for (Stmt l = root_; l.as<For>().has_value(); l = l.as<For>().value()->body)
    loop_total_size = loop_total_size * l.as<For>().value()->extent;
  DLOG(INFO) << "[PlanLoopPartition] loop_total_size = " << loop_total_size
             << '\n';
  bool has_fragment_access = !indice_map_.empty();
  if (has_fragment_access) {
    while (!analyzer_.CanProve(
               floormod(loop_total_size, layout_args.thread_bounds->extent *
                                             vector_size) == 0) &&
           vector_size > 1) {
      vector_size /= 2;
    }
  } else if (!root_->annotations.count(attr::kCoalescedWidth)) {
    vector_size = SelectMinPaddingVectorSize(vector_size, loop_total_size,
                                             layout_args.thread_bounds->extent,
                                             &analyzer_);
  }
  DLOG(INFO) << "[PlanLoopPartition] after adjust: vector_size = "
             << vector_size << '\n';

  // Check if coalesced_width is defined
  if (auto coalesced_width = root_->annotations.Get(attr::kCoalescedWidth)) {
    if (const auto *imm = coalesced_width->as<IntImmNode>()) {
      int expected = imm->value;
      // Verify that vector_size is divisible by expected
      if (vector_size % expected != 0) {
        LOG(FATAL) << "Vector size " << vector_size
                   << " is not divisible by coalesced width " << expected;
      }
      vector_size = expected;
    } else {
      LOG(FATAL) << "coalesced_width should be an IntImmNode.";
    }
  }
  DLOG(INFO) << "[PlanLoopPartition] root_ = " << root_
             << " ############# vector_size = " << vector_size
             << ", thread_bounds = " << layout_args.thread_bounds << '\n';
  auto plan = PlanLoopPartition(root_, vector_size, layout_args.thread_bounds);
  DLOG(INFO) << "[PlanLoopPartition] candidate = " << plan->DebugOutput()
             << '\n';
  return plan;
}

void ParallelOpNode::BuildReplicationGuardsIfNeeded(
    const LayoutInferArgs &layout_args,
    const std::vector<Buffer> &store_shared_global_buffers,
    const std::vector<Buffer> &store_fragment_buffers,
    bool has_cross_thread_access,
    const std::vector<Buffer> &const_index_fragment_buffer) const {
  if (is_one(loop_layout_->ReplicateExtent()))
    return;
  if (!has_cross_thread_access)
    return;

  if (!store_fragment_buffers.empty()) {
    bool replicate_is_from_dynamic_index_fragment = false;
    for (const auto &fragment : store_fragment_buffers) {
      if (!layout_args.layout_map.count(fragment)) {
        continue;
      }

      auto fragment_layout =
          layout_args.layout_map[fragment].as<Fragment>().value();
      if (is_one(fragment_layout->ReplicateExtent()))
        continue;

      if (analyzer_.CanProveEqual(fragment_layout->ReplicateExtent(),
                                  loop_layout_->ReplicateExtent()))
        continue;
      if (std::find(const_index_fragment_buffer.begin(),
                    const_index_fragment_buffer.end(),
                    fragment) == const_index_fragment_buffer.end()) {
        replicate_is_from_dynamic_index_fragment = true;
      }
    }

    if (!replicate_is_from_dynamic_index_fragment)
      return;

    ICHECK(store_shared_global_buffers.empty())
        << "Invalid layout: cannot have both fragment and shared store buffers "
           "in replicated loop layout.";
    return;
  } else {
    auto inv = loop_layout_->Inverse();
    Array<PrimExpr> fwd;
    for (size_t i = 0; i < loop_layout_->OutputDim(); i++)
      fwd.push_back(0);
    fwd.push_back(InputPlaceholder(0) - layout_args.thread_bounds->min);
    auto rep = inv->Forward(fwd).back();
    AddPredicate(EQ(rep, 0));
  }
}
Fragment
ParallelOpNode::ChooseBestCandidate(const Fragment &candidate_from_buffer,
                                    const Fragment &candidate_from_plan,
                                    const LayoutInferArgs &layout_args) const {
  // Strategy overview:
  // 1) Validate each candidate against all known source fragments. If only one
  //    is compatible, choose it immediately.
  // 2) If both are compatible, compare their containment relation:
  //      - If buffer-based contains plan-based, prefer plan (usually smaller
  //      rep).
  //      - If plan-based contains buffer-based, prefer buffer.
  // 3) If neither contains the other, prefer the one with provably smaller or
  //    equal replication extent; otherwise fall back to buffer-based candidate.
  // Note: Final global validation happens after selection elsewhere.
  auto vars =
      loop_vars_.Map([](const IterVar &iv) { return PrimExpr(iv->var); });
  auto contains = [&](const Fragment &big, const Fragment &small) {
    // contains(A, B) means: for any loop index, the threads that access
    // B's elements are a subset of those that access A's elements.
    return ProveFragmentContains(small, big, vars, vars, analyzer_);
  };

  bool buf_ok =
      ValidateCandidateAgainstFragments(candidate_from_buffer, layout_args);
  bool plan_ok =
      ValidateCandidateAgainstFragments(candidate_from_plan, layout_args);

  if (buf_ok && !plan_ok) {
    DLOG(INFO)
        << "[FreeInfer] prefer compute_from_buffer (only valid candidate).";
    return candidate_from_buffer;
  }
  if (plan_ok && !buf_ok) {
    DLOG(INFO)
        << "[FreeInfer] prefer PlanLoopPartition (only valid candidate).";
    return candidate_from_plan;
  }
  if (!(buf_ok && plan_ok)) {
    // Both invalid here; let the caller continue to final validation/throw.
    // Returning buffer-based candidate keeps behavior deterministic.
    return candidate_from_buffer; // arbitrary; caller will catch later
  }

  // Both candidates may be semantically valid for reads from a fully
  // replicated fragment.  The plan candidate often minimizes replication by
  // assigning one logical element to each CUDA thread, but that can turn a
  // register fragment access into `fragment[threadIdx.x]`.  CUDA cannot keep a
  // register array with runtime indexing in scalar registers, so this shape
  // spills the whole fragment to local memory before the final store.
  //
  // When this parallel loop publishes data to shared/global memory, prefer the
  // valid candidate that keeps fragment physical indices independent of the
  // thread variable.  For a fully replicated readback this selects the
  // fragment-derived layout, and BuildReplicationGuardsIfNeeded then emits the
  // single-replica guard (e.g. `threadIdx.x == 0`) to avoid duplicate stores.
  // Fragment-only loops keep the old containment/replication heuristic below.
  if (!store_shared_global_buffers_.empty()) {
    bool buf_thread_index =
        HasThreadDependentFragmentIndex(candidate_from_buffer, layout_args);
    bool plan_thread_index =
        HasThreadDependentFragmentIndex(candidate_from_plan, layout_args);
    if (buf_thread_index != plan_thread_index) {
      return buf_thread_index ? candidate_from_plan : candidate_from_buffer;
    }
  }

  bool buf_contains_plan = contains(candidate_from_buffer, candidate_from_plan);
  bool plan_contains_buf = contains(candidate_from_plan, candidate_from_buffer);

  auto rep_buf = candidate_from_buffer->ReplicateExtent();
  auto rep_plan = candidate_from_plan->ReplicateExtent();

  // Prefer the contained candidate (tends to minimize replication while
  // respecting access coverage):
  if (buf_contains_plan && !plan_contains_buf) {
    return candidate_from_plan;
  }
  if (plan_contains_buf && !buf_contains_plan) {
    return candidate_from_buffer;
  }
  // Neither strictly contains the other; prefer the one with smaller/equal rep.
  if (analyzer_.CanProve(rep_plan <= rep_buf)) {
    return candidate_from_plan;
  }
  // Safe fallback: buffer-based candidate is always correct.
  return candidate_from_buffer;
}

bool ParallelOpNode::HasThreadDependentFragmentIndex(
    const Fragment &candidate, const LayoutInferArgs &layout_args) const {
  // Mirror the index substitution that PartitionLoop would apply for this
  // candidate without actually rebuilding the loop.  We create symbolic output
  // coordinates plus a symbolic thread variable, invert the candidate layout
  // back to the original loop variables, and then apply each known fragment
  // layout's Forward() map.  If any resulting physical fragment index still
  // contains the symbolic thread variable, lowering this candidate would emit a
  // runtime-thread-indexed register array access such as
  // `fragment[threadIdx.x]`.
  if (!candidate.defined()) {
    return false;
  }

  int old_loop_depth = static_cast<int>(loop_vars_.size());
  if (candidate->InputDim() != static_cast<size_t>(old_loop_depth)) {
    return false;
  }

  Var thread_var("__tl_candidate_thread");
  Array<PrimExpr> partition_vars;
  for (size_t i = 0; i < candidate->OutputDim(); ++i) {
    partition_vars.push_back(Var("__tl_candidate_i" + std::to_string(i)));
  }
  partition_vars.push_back(thread_var);

  auto inv_loop = candidate->InverseWithLevel().first;
  Array<PrimExpr> recovered_indices = inv_loop->Forward(partition_vars);

  Map<Var, PrimExpr> loop_var_map;
  for (int i = 0; i < old_loop_depth; ++i) {
    loop_var_map.Set(loop_vars_[i]->var, recovered_indices[i]);
  }

  Map<Var, PrimExpr> thread_offset_map;
  bool has_thread_offset = false;
  if (candidate->ThreadRange().defined()) {
    auto range = candidate->ThreadRange();
    thread_offset_map.Set(thread_var, thread_var - range->min);
    has_thread_offset = true;
  }

  auto uses_thread_var = [&](const PrimExpr &expr) {
    PrimExpr simplified = analyzer_.Simplify(expr);
    return tirx::UsesVar(simplified, [&](const VarNode *var_node) {
      return GetRef<Var>(var_node).same_as(thread_var);
    });
  };

  for (const auto &buffer : access_order_) {
    if (!IsFragmentBuffer(buffer) || !layout_args.layout_map.count(buffer)) {
      continue;
    }
    auto fragment_layout = layout_args.layout_map[buffer].as<Fragment>();
    if (!fragment_layout.has_value()) {
      continue;
    }

    Array<PrimExpr> indices = GetAccessInfo(buffer).indices.Map(
        [&](const PrimExpr &index) { return Substitute(index, loop_var_map); });
    if (has_thread_offset) {
      indices = Substitute(indices, thread_offset_map);
    }

    Array<PrimExpr> physical_indices =
        fragment_layout.value()->Forward(indices);
    for (const auto &index : physical_indices) {
      if (uses_thread_var(index)) {
        return true;
      }
    }
  }

  return false;
}

